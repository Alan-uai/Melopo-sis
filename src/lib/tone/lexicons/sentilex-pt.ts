export const NEGATION_WORDS = new Set([
  'não', 'nem', 'nunca', 'jamais', 'ninguém', 'ninguem', 'nada', 'nenhum',
  'nenhuma', 'tampouco', 'sequer', 'sem',
]);

export const INTENSIFIERS: Record<string, number> = {
  'muito': 0.8, 'tanto': 0.7, 'tão': 0.6, 'bem': 0.3,
  'extremamente': 0.9, 'completamente': 0.8, 'totalmente': 0.8,
  'pouco': -0.5, 'levemente': -0.3, 'um pouco': -0.3,
  'imensamente': 0.9, 'profundamente': 0.8, 'absolutamente': 0.9,
};

function buildLexicon(): Map<string, SentiLexEntry> {
  const map = new Map<string, SentiLexEntry>();
  for (const [lemma, polarity, pos] of RAW_LEXICON) {
    map.set(lemma, { lemma, polarity, pos });
  }
  return map;
}

const LEXICON = buildLexicon();

export function getSentiment(word: string): SentiLexEntry | undefined {
  return LEXICON.get(word.toLowerCase());
}

function isNegated(tokens: string[], idx: number): boolean {
  const start = Math.max(0, idx - 4);
  for (let i = start; i < idx; i++) {
    if (NEGATION_WORDS.has(tokens[i].toLowerCase())) return true;
  }
  return false;
}

function getIntensifierBoost(tokens: string[], idx: number): number {
  const start = Math.max(0, idx - 2);
  let boost = 0;
  for (let i = start; i < idx; i++) {
    const val = INTENSIFIERS[tokens[i].toLowerCase()];
    if (val !== undefined) boost += val;
  }
  return Math.min(boost, 1.5);
}

export function analyzeSentiment(tokens: string[]): {
  positiveRatio: number;
  negativeRatio: number;
  neutralRatio: number;
  compoundScore: number;
  dominantEmotion: string;
} {
  let posScore = 0, negScore = 0, neuCount = 0;

  for (let i = 0; i < tokens.length; i++) {
    const entry = LEXICON.get(tokens[i].toLowerCase());
    if (!entry) { neuCount++; continue; }

    let polarity: number = entry.polarity;
    const negated = isNegated(tokens, i);
    const intensifier = getIntensifierBoost(tokens, i);

    if (negated) {
      polarity = -(polarity * 0.74);
    }

    if (intensifier !== 0) {
      polarity += polarity * intensifier;
    }

    if (polarity > 0) posScore += polarity;
    else if (polarity < 0) negScore += Math.abs(polarity);
    else neuCount++;
  }

  const total = posScore + negScore + neuCount || 1;
  const alpha = 15;
  const compound = (posScore - negScore) / Math.sqrt(Math.pow(posScore + negScore, 2) + alpha);

  let dominantEmotion: string;
  if (compound > 0.05) dominantEmotion = 'positivo';
  else if (compound < -0.05) dominantEmotion = 'negativo';
  else dominantEmotion = 'neutro';

  return {
    positiveRatio: posScore / total,
    negativeRatio: negScore / total,
    neutralRatio: neuCount / total,
    compoundScore: compound,
    dominantEmotion,
  };
}
